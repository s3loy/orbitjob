func TestVerifySpec(t *testing.T) {
	tmpDir := t.TempDir()
	path := filepath.Join(tmpDir, "spec.yaml")
	generated := []byte("openapi: 3.0.3\n")

	if err := os.WriteFile(path, generated, 0o644); err != nil {
		t.Fatalf("write seed file: %v", err)
	}
	if err := verifySpec(path, generated); err != nil {
		t.Fatalf("verifySpec expected success, got %v", err)
	}

	err := verifySpec(path, []byte("openapi: 3.1.0\n"))
	if err == nil || !strings.Contains(err.Error(), "openapi spec drift detected") {
		t.Fatalf("expected drift error, got %v", err)
	}

	err = verifySpec(filepath.Join(tmpDir, "missing.yaml"), generated)
	if err == nil || !strings.Contains(err.Error(), "read") {
		t.Fatalf("expected read error for missing file, got %v", err)
	}
}

func TestWriteSpec(t *testing.T) {
	path := filepath.Join(t.TempDir(), "nested", "spec.yaml")
	generated := []byte("openapi: 3.0.3\n")

	if err := writeSpec(path, generated); err != nil {
		t.Fatalf("writeSpec: %v", err)
	}

	written, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read written spec: %v", err)
	}
	if !bytes.Equal(written, generated) {
		t.Fatalf("expected written spec to match generated bytes")
	}
}

