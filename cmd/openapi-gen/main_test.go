func TestMain_WriteAndCheckModes(t *testing.T) {
	outPath := filepath.Join(t.TempDir(), "openapi.yaml")

	runMainWithArgs(t, "-out", outPath)
	content, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("read generated spec: %v", err)
	}
	if !bytes.HasSuffix(content, []byte("\n")) {
		t.Fatalf("expected generated file to end with newline")
	}

	runMainWithArgs(t, "-check", "-out", outPath)
}

