func TestRenderOpenAPIYAML(t *testing.T) {
	rendered, err := renderOpenAPIYAML(adminhttp.ServiceOpenAPIDocument())
	if err != nil {
		t.Fatalf("renderOpenAPIYAML: %v", err)
	}
	if !bytes.Contains(rendered, []byte("openapi: 3.0.3")) {
		t.Fatalf("expected yaml to contain openapi version, got:\n%s", string(rendered))
	}
	if !bytes.HasSuffix(rendered, []byte("\n")) {
		t.Fatalf("expected yaml output to end with newline")
	}
}

