func runMainWithArgs(t *testing.T, args ...string) {
	t.Helper()

	oldArgs := os.Args
	oldCommandLine := flag.CommandLine

	os.Args = append([]string{"openapi-gen"}, args...)
	flag.CommandLine = flag.NewFlagSet(os.Args[0], flag.ContinueOnError)

	oldRender := renderOpenAPIYAMLFn
	oldVerify := verifySpecFn
	oldWrite := writeSpecFn
	oldExit := exitFn
	oldJSONToYAML := jsonToYAML

	defer func() {
		os.Args = oldArgs
		flag.CommandLine = oldCommandLine
		renderOpenAPIYAMLFn = oldRender
		verifySpecFn = oldVerify
		writeSpecFn = oldWrite
		exitFn = oldExit
		jsonToYAML = oldJSONToYAML
	}()

	main()
}

func runMainExpectExit(t *testing.T, wantCode int, args []string, run func()) {
	t.Helper()

	oldArgs := os.Args
	oldCommandLine := flag.CommandLine
	oldRender := renderOpenAPIYAMLFn
	oldVerify := verifySpecFn
	oldWrite := writeSpecFn
	oldExit := exitFn
	oldJSONToYAML := jsonToYAML

	os.Args = append([]string{"openapi-gen"}, args...)
	flag.CommandLine = flag.NewFlagSet(os.Args[0], flag.ContinueOnError)
	exitFn = func(code int) {
		panic(exitPanic{code: code})
	}

	defer func() {
		os.Args = oldArgs
		flag.CommandLine = oldCommandLine
		renderOpenAPIYAMLFn = oldRender
		verifySpecFn = oldVerify
		writeSpecFn = oldWrite
		exitFn = oldExit
		jsonToYAML = oldJSONToYAML
	}()

	defer func() {
		r := recover()
		if r == nil {
			t.Fatalf("expected main to exit with code %d", wantCode)
		}
		exit, ok := r.(exitPanic)
		if !ok {
			t.Fatalf("expected exit panic, got %T (%v)", r, r)
		}
		if exit.code != wantCode {
			t.Fatalf("expected exit code %d, got %d", wantCode, exit.code)
		}
	}()

	run()
}
